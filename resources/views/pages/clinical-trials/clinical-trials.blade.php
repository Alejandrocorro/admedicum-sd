                                    <br><br>
                                    The US based Clinical Trials Transformation Initiative (CTTI) has also developed some useful tools:
                                    <br><br>
                                    <a target="_blank" href="https://www.ctti-clinicaltrials.org/files/recruitment-tool-1-decisiontree.pdf">CTTI Recruitment Tool Decisiontree</a> provides a checklist that helps you to define “when” to involve other stakeholders.
                                    <br><br>
                                    <a target="_blank" href="https://www.ctti-clinicaltrials.org/files/recruitment-tool-2-stakeholder.pdf">CTTI Recruitment Tool Stakeholder</a> helps you to prepare a strategy regarding which stakeholders to approach and for what reasons. 
                                    <br><br>
                                    Keep in mind that stakeholder involvement means really giving patients a chance to understand and express themselves, rather than guessing what a minimally burdensome trial design is. 
                                    <br><br>
                                    The patient knows best
                                    <br><br>
                                    Benefits of co-creation with patients in clinical trials
                                    Involving patients in clinical trial planning yields a number of opportunities that go beyond successful patient recruitment and retention alone. Examples of involving patient in clinical trials include: the critical review of primary and secondary clinical trial endpoints, patient reported outcomes and the patient view on benefit-risk assessment. However, all of this requires resources, time and budget. Therefore getting senior management buy-in early on is a key to success to increase enrollment in clinical trials.
                                    <br><br>
                                    Communicating to clinical trial participants
                                    Remember that although there is an obligation to publish your clinical trial, this does not provide individual trial participants with information regarding “their” participation in the clinical trial. Please don’t forget the patient experts you worked with, once the collaboration comes to an end. 
                                    <br><br>
                                    Prepare final reports containing information regarding what suggestions will be implemented, which ones will not, and provide reasons why. It is good to have agreement on feedback to involved patient experts before the clinical trial begins. And again, patient experts and investigators can probably tell you best what they would like to know from this excersise. 
