                                <p class="card-content-close font-is-20 has-text-grey tk-canada-type-gibson is-full-width">There are many examples, including insights from <a target="_blank" href="https://www.fda.gov/drugs/development-approval-process-drugs/cder-patient-focused-drug-development"> FDA</a> and<a target="_blank" href="https://www.ema.europa.eu/en/partners-networks/patients-consumers"> EMA</a>, on the positive impact of patient engagement on recruitment and retention in trials.</p>
                                <div class="font-is-20 card-content-open-2 has-text-grey tk-canada-type-gibson is-full-width">There are many examples, including insights from FDA and EMA, on the positive impact of patient engagement on recruitment and retention in trials. Both aspects are often negatively influenced by the logistics of the trial and frequent visits to the investigator site, which disrupt a normal routine or work. Those logistical challenges and the COVID pandemic have led to a rise in decentralized clinical trials. Patient involvement in the design of clinical trial is therefore highly recommended and has become common practice in the industry. 
                                    <br><br>
                                    Evidence for patient engagement in clinical trials
                                    <br><br>
                                    <a target="_blank" href="http://pemsuite.org/How-to-Guides/Patient-engagement-in-clinical-trial-protocol-design.pdf">How to Guide on Patient Engagement in Clinical Trial Design</a> by PFMD
                                    <br><br>
                                    The <a target="_blank" href="http://www.diaglobal.org/en/resources/press-releases/2016/10-31-patient-engagement-study">DIA Patient Engagement Study</a> reported reduced screen failure rates, faster patient recruitment rates, improved subject retention rates, reduced numbers of protocol amendments, and a greater number of patient relevant endpoints as positive aspects of patient involvement.
                                    <br><br>
                                    The <a target="_blank" href="https://www.fda.gov/downloads/AdvisoryCommittees/CommitteesMeetingMaterials/PatientEngagementAdvisoryCommittee/UCM579153.pdf">FDA Executive Summary Patient Engagement Advisory Committee</a> leads you to further publications and materials that give guidance regarding patient involvement related to clinical trials.
                                    <br><br>
                                    The <a target="_blank" href="https://csdd.tufts.edu/impact-reports/">Tufts Center for Drug Development Impact Report</a> reported that 57% of all protocols, across all phases, have at least one substantial global amendment. The most frequent changes stem from amendments associated with modifications and revisions to study volunteer demographics and eligibility criteria. Based on the additional time and costs, it seems more cost efficient to reduce amendments by involving patients early on in the trial design and protocol discussions. 
                                    <br><br>
                                   <a target="_blank" href="https://toolbox.eupati.eu/resources/patients-involved-collaboration-in-an-oncology-trial/">EUPATI's article on collaboration in an oncology trial</a> shows another good case study for insightful involvement of patients in an oncology trial by Sanofi in France.</div>
